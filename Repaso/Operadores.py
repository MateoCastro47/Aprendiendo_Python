#Operadores

"""
+ (Suma)
- (Resta)
* (Producto)
/ (División)
** (Exponente)
"""

#Suma
print(3+4)
#Resta
print(3-4)
#Producto
print(3*4)
#Division
print(3/4)
#Exponente
print(3**4)

#Puedes multiplicar cadenas de texto
print("Hola " * 3)
#Si creas una variable también puede multiplicar esa clave de texto
my_float = 4 * 2
print("Hola " * my_float)
#Operadores Comparativos

#Menor que
print(3<4)
#Mayor que
print(4>3)
#Igual a
print(4 == 3)
#Menor o igual que
print(3<=4)
#Mayor o igual que
print(3>=4)
#Diferente a
print(3!=4)
print("Aquí hay un cambio de ejercicio")
#Todos estos print devuelven un valor booleano como True o False.
#También además de comparar números phyton puede comparar Strings esto es:
print("Hola" > "Phyton")
print("Hola" < "Phyton")
print("Hola" == "Zola") #Ordenación alfabética inversa, es decir, a<b<c<d, etc...
print(len("Hola") <= len("Pola")) #Cuenta caracteres.
print("Hola" >= "Zola")
print("Hola" != "Phyton")

#Operadores Lógicos

### Existen tres operadores lógicos ###
print("Hola" > "Phyton" and 3 < 4) #También se puede escribir como &&
print("Hola" < "Phyton" or 3 > 4) #También se puede escribir como ||

